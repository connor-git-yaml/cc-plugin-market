import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { parseYamlDocument } from './lib/simple-yaml.mjs';
import { computeWrapperBodySha256 } from './lib/extract-wrapper-body.mjs';

function parseArgs(argv) {
  const options = {
    projectRoot: process.cwd(),
    json: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--project-root') {
      const value = argv[index + 1];
      if (!value) {
        throw new Error('缺少 --project-root 参数值');
      }
      options.projectRoot = path.resolve(value);
      index += 1;
      continue;
    }
    if (arg === '--json') {
      options.json = true;
      continue;
    }
    if (arg === '--help' || arg === '-h') {
      options.help = true;
      continue;
    }
    throw new Error(`未知参数: ${arg}`);
  }

  return options;
}

function createCheck(id, title, status, evidence = {}) {
  return { id, title, status, evidence };
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf-8'));
}

function ensureFileExists(filePath, message) {
  if (!fs.existsSync(filePath)) {
    throw new Error(message ?? `缺少文件: ${filePath}`);
  }
}

export function loadWrapperSourceContract(projectRoot) {
  const scriptDir = path.dirname(fileURLToPath(import.meta.url));
  const contractPath = path.resolve(scriptDir, '..', 'contracts', 'wrapper-source-of-truth.yaml');
  const contract = parseYamlDocument(fs.readFileSync(contractPath, 'utf-8'));
  return {
    projectRoot: path.resolve(projectRoot),
    contractPath,
    contract,
  };
}

function validateWrapperMarkers(projectRoot, entries, errors) {
  const requiredFragments = [
    '## Wrapper Source Contract',
    'Generated by:',
    'Contract:',
    'do not edit this wrapper directly',
  ];
  const checked = [];
  const missingFiles = [];
  const invalidMarkers = [];
  // F186 T2：wrapper body sha256 漂移检测
  const shaMismatches = [];
  const missingSha = [];

  for (const entry of entries) {
    const targetPath = path.resolve(projectRoot, entry.target);
    if (!fs.existsSync(targetPath)) {
      missingFiles.push(entry.target);
      continue;
    }

    const content = fs.readFileSync(targetPath, 'utf-8');
    const missingFragments = requiredFragments.filter((fragment) => !content.includes(fragment));
    const sourceMarker = `Canonical source: \`$PLUGIN_DIR/skills/${entry.id}/SKILL.md\``;
    const contractMarker = 'Contract: `$PLUGIN_DIR/contracts/wrapper-source-of-truth.yaml`';
    if (!content.includes(sourceMarker)) {
      missingFragments.push(sourceMarker);
    }
    if (!content.includes(contractMarker)) {
      missingFragments.push(contractMarker);
    }

    if (missingFragments.length > 0) {
      invalidMarkers.push({
        target: entry.target,
        missingFragments,
      });
      continue;
    }

    // F186 T2：解析 wrapper 内嵌的 Source SHA256，对 source 用同一 helper 重算比对。
    // 缺 `Source SHA256:` 行 → fail（FINAL CRITICAL-2：缺 sha 即视为漂移/被手改，不是 warn）。
    const shaMatch = content.match(/^- Source SHA256:\s*([0-9a-f]{64})\s*$/m);
    if (!shaMatch) {
      missingSha.push(entry.target);
      continue;
    }
    const sourcePath = path.resolve(projectRoot, entry.source);
    if (!fs.existsSync(sourcePath)) {
      // source 缺失由 source-skills check 单独报；此处不重复，但 sha 无法校验 → 视为不通过
      shaMismatches.push({ target: entry.target, expected: shaMatch[1], actual: '<source-missing>' });
      continue;
    }
    const actualSha = computeWrapperBodySha256(sourcePath);
    if (actualSha !== shaMatch[1]) {
      shaMismatches.push({ target: entry.target, expected: shaMatch[1], actual: actualSha });
      continue;
    }

    checked.push(entry.target);
  }

  if (missingFiles.length > 0) {
    errors.push(
      `缺少 Codex 包装技能，请先运行 npm run codex:spec-driver:install：${missingFiles.join(', ')}`,
    );
  }

  if (invalidMarkers.length > 0) {
    errors.push(
      `以下 Codex 包装技能缺少 wrapper source contract 标记：${invalidMarkers
        .map((item) => `${item.target} [${item.missingFragments.join('; ')}]`)
        .join(', ')}`,
    );
  }

  if (missingSha.length > 0) {
    errors.push(
      `以下 Codex 包装技能缺少 \`Source SHA256:\` 行（疑似漂移/被手改，请重新运行 npm run codex:spec-driver:install）：${missingSha.join(', ')}`,
    );
  }

  if (shaMismatches.length > 0) {
    errors.push(
      `以下 Codex 包装技能 body sha256 不匹配（source 已变更但 wrapper 未重生成）：${shaMismatches
        .map((item) => `${item.target}（期望 ${item.expected.slice(0, 12)}…，实际 ${item.actual.slice(0, 12)}…）`)
        .join(', ')}`,
    );
  }

  const ok =
    missingFiles.length === 0 &&
    invalidMarkers.length === 0 &&
    missingSha.length === 0 &&
    shaMismatches.length === 0;

  return createCheck(
    'codex-wrapper-markers',
    'Codex 包装技能 source-of-truth 标记',
    ok ? 'pass' : 'fail',
    {
      checkedCount: checked.length,
      missingFiles,
      invalidMarkers,
      missingSha,
      shaMismatches,
    },
  );
}

function validateContract(projectRoot, contract) {
  const errors = [];
  const warnings = [];
  const checks = [];

  const pluginMetadataPath = path.resolve(projectRoot, contract.plugin?.metadataSource ?? '');
  const marketplacePath = path.resolve(projectRoot, contract.plugin?.marketplaceManifest ?? '');
  ensureFileExists(pluginMetadataPath, `缺少 plugin metadata: ${contract.plugin?.metadataSource ?? ''}`);
  ensureFileExists(
    marketplacePath,
    `缺少 marketplace manifest: ${contract.plugin?.marketplaceManifest ?? ''}`,
  );

  const codexEntries = Array.isArray(contract.codexWrappers?.entries)
    ? contract.codexWrappers.entries
    : [];
  const claudeEntries = Array.isArray(contract.claudeProjectOverrides?.entries)
    ? contract.claudeProjectOverrides.entries
    : [];

  const missingSourceFiles = codexEntries
    .filter((entry) => !fs.existsSync(path.resolve(projectRoot, entry.source)))
    .map((entry) => entry.source);
  if (missingSourceFiles.length > 0) {
    errors.push(`缺少 canonical source skill: ${missingSourceFiles.join(', ')}`);
  }
  checks.push(
    createCheck('source-skills', 'Canonical source skills', missingSourceFiles.length === 0 ? 'pass' : 'fail', {
      count: codexEntries.length,
      missing: missingSourceFiles,
    }),
  );

  checks.push(validateWrapperMarkers(projectRoot, codexEntries, errors));

  const missingClaudeOverrides = claudeEntries
    .filter((entry) => !fs.existsSync(path.resolve(projectRoot, entry.target)))
    .map((entry) => entry.target);
  if (missingClaudeOverrides.length > 0) {
    errors.push(`缺少 Claude project override: ${missingClaudeOverrides.join(', ')}`);
  }
  checks.push(
    createCheck(
      'claude-project-overrides',
      'Claude 项目级 override',
      missingClaudeOverrides.length === 0 ? 'pass' : 'fail',
      {
        count: claudeEntries.length,
        missing: missingClaudeOverrides,
      },
    ),
  );

  const pluginMetadata = readJson(pluginMetadataPath);
  const marketplace = readJson(marketplacePath);
  const marketplaceEntry = Array.isArray(marketplace.plugins)
    ? marketplace.plugins.find((entry) => entry.name === pluginMetadata.name)
    : null;

  if (!marketplaceEntry) {
    errors.push(`marketplace.json 中缺少插件条目: ${pluginMetadata.name}`);
  } else {
    const expectedSource = `./${contract.plugin.sourceRoot}`;
    if (marketplaceEntry.source !== expectedSource) {
      errors.push(
        `marketplace 插件 source 不匹配：期望 ${expectedSource}，实际 ${marketplaceEntry.source}`,
      );
    }
    if (marketplaceEntry.version !== pluginMetadata.version) {
      errors.push(
        `marketplace 插件 version 不匹配：期望 ${pluginMetadata.version}，实际 ${marketplaceEntry.version}`,
      );
    }
    if (marketplaceEntry.name !== pluginMetadata.name) {
      errors.push(
        `marketplace 插件 name 不匹配：期望 ${pluginMetadata.name}，实际 ${marketplaceEntry.name}`,
      );
    }
  }

  checks.push(
    createCheck(
      'plugin-metadata-sync',
      'Plugin metadata 与 marketplace 同步',
      marketplaceEntry && errors.every((error) => !error.includes('marketplace 插件') && !error.includes('marketplace.json 中缺少插件条目'))
        ? 'pass'
        : 'fail',
      {
        pluginName: pluginMetadata.name,
        version: pluginMetadata.version,
        sourceRoot: contract.plugin.sourceRoot,
      },
    ),
  );

  const status = errors.length > 0 ? 'fail' : warnings.length > 0 ? 'warn' : 'pass';
  return {
    schemaVersion: contract.schemaVersion ?? 1,
    status,
    contractPath: path.relative(projectRoot, path.resolve(projectRoot, 'plugins/spec-driver/contracts/wrapper-source-of-truth.yaml')).split(path.sep).join('/'),
    checks,
    warnings,
    errors,
  };
}

function formatTextResult(result) {
  const lines = [
    `[wrapper-source] status=${result.status}`,
    `contract: ${result.contractPath}`,
    '',
  ];

  for (const check of result.checks) {
    lines.push(`- ${check.id}: ${check.status}`);
  }

  if (result.warnings.length > 0) {
    lines.push('', 'warnings:');
    for (const warning of result.warnings) {
      lines.push(`  - ${warning}`);
    }
  }

  if (result.errors.length > 0) {
    lines.push('', 'errors:');
    for (const error of result.errors) {
      lines.push(`  - ${error}`);
    }
  }

  return lines.join('\n');
}

export function validateWrapperSources(options = {}) {
  // realpathSync 解决 macOS /var → /private/var symlink 导致 path.relative 计算错误的问题
  const projectRoot = fs.realpathSync(path.resolve(options.projectRoot ?? process.cwd()));
  const { contractPath, contract } = loadWrapperSourceContract(projectRoot);
  const result = validateContract(projectRoot, contract);
  return {
    ...result,
    contractPath: path.relative(projectRoot, contractPath).split(path.sep).join('/'),
  };
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  if (options.help) {
    console.log('用法: node plugins/spec-driver/scripts/validate-wrapper-sources.mjs --project-root . [--json]');
    return;
  }

  const result = validateWrapperSources(options);

  if (options.json) {
    console.log(JSON.stringify(result, null, 2));
  } else {
    console.log(formatTextResult(result));
  }

  if (result.status === 'fail') {
    process.exitCode = 1;
  }
}

function isDirectExecution() {
  if (!process.argv[1]) {
    return false;
  }
  return fs.realpathSync(process.argv[1]) === fs.realpathSync(fileURLToPath(import.meta.url));
}

if (isDirectExecution()) {
  main();
}
